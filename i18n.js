  var STORE_KEY = "unimind_lang";

  function getLang() {
    var saved;
    try { saved = localStorage.getItem(STORE_KEY); } catch (e) {}
    return (saved === "en" || saved === "id") ? saved : "id";
  }

  function t(key) {
    var lang = window.UNIMIND_LANG || getLang();
    var dict = I18N[lang] || I18N.id;
    return (key in dict) ? dict[key] : (I18N.id[key] || key);
  }

  function applyLang(lang) {
    if (lang !== "en" && lang !== "id") lang = "id";
    var dict = I18N[lang];
    window.UNIMIND_LANG = lang;
    try { localStorage.setItem(STORE_KEY, lang); } catch (e) {}

    document.documentElement.setAttribute("lang", lang);
    if (dict["doc.title"]) document.title = dict["doc.title"];

    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var key = el.getAttribute("data-i18n");
      if (key in dict) el.innerHTML = dict[key];
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
      var key = el.getAttribute("data-i18n-placeholder");
      if (key in dict) el.setAttribute("placeholder", dict[key]);
    });

    document.querySelectorAll(".lang-opt").forEach(function (btn) {
      var active = btn.getAttribute("data-lang") === lang;
      btn.classList.toggle("active", active);
      btn.setAttribute("aria-pressed", String(active));
    });

    // let other scripts react (e.g. live form status text)
    window.dispatchEvent(new CustomEvent("unimind:langchange", { detail: { lang: lang } }));
  }

  // Wire toggle buttons
  document.querySelectorAll(".lang-opt").forEach(function (btn) {
    btn.addEventListener("click", function () {
      applyLang(btn.getAttribute("data-lang"));
    });
  });

  // expose for script.js
  window.UnimindI18N = { t: t, applyLang: applyLang, getLang: getLang };

  // initial
  applyLang(getLang());
